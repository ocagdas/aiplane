def _refresh_model_row(
    name: str,
    model: dict[str, Any],
    runtime_catalog: Any,
    refresh_status: str,
    provider_visible: bool | None,
    provider_reason: str,
) -> dict[str, Any]:
    source = runtime_catalog.source_for_model(model)
    preferred = model.get("preferred_runtime") or model.get("provider")
    provider_name = str(model.get("provider") or "")
    provider = runtime_catalog._providers().get(provider_name, {})
    ownership = ownership_for_model(model, provider)
    row = {
        "name": name,
        "model": {"id": model.get("model"), "source": source},
        "runtime_endpoint": provider_name,
        "ownership": ownership,
        "refresh_status": refresh_status,
        "enabled": bool(model.get("enabled", True)),
        "provider_visible": provider_visible,
        "local_presence": _refresh_local_presence(ownership, provider_name, provider_visible),
        "provider_reason": provider_reason,
        "preferred_runtime": preferred,
        "suitable_runtimes": runtime_catalog.compatible_runtimes_for_entry(model),
    }
    for key in [
        "roles",
        "local",
        "api_key_env",
        "pull_command",
        "min_ram_gb",
        "recommended_ram_gb",
        "min_vram_gb",
        "recommended_vram_gb",
        "context_window_tokens",
        "max_output_tokens",
        "supports_tool_calling",
        "supports_streaming",
        "notes",
    ]:
        if key in model and key not in row:
            row[key] = model[key]
    return row


def _refresh_local_presence(ownership: str, provider_name: str, provider_visible: bool | None) -> str:
    if ownership == "managed_service":
        return "not_applicable_managed_service"
    if provider_visible is True:
        return "present" if provider_name != "ollama" else "pulled"
    if provider_visible is False:
        return "not_listed" if provider_name != "ollama" else "not_pulled"
    return "not_checked"


def _group_refresh_catalog_rows(
    rows: list[dict[str, Any]],
) -> dict[str, dict[str, list[dict[str, Any]]]]:
    grouped: dict[str, dict[str, list[dict[str, Any]]]] = {}
    for row in rows:
        ownership = str(row.get("ownership") or "unknown")
        provider = str(
            (row.get("model") or {}).get("source")
            if isinstance(row.get("model"), dict)
            else row.get("source") or "unknown"
        )
        grouped.setdefault(ownership, {}).setdefault(provider, []).append(row)
    return {
        ownership: {
            provider: sorted(items, key=lambda item: str(item.get("name") or ""))
            for provider, items in sorted(providers.items())
        }
        for ownership, providers in sorted(grouped.items())
    }


def _unique_model_alias(models: dict[str, Any], provider_name: str, model_id: str) -> str:
    import re

    base = re.sub(r"[^a-z0-9]+", "-", model_id.lower()).strip("-")
    if provider_name and not base.startswith(provider_name + "-"):
        base = f"{provider_name}-{base}"
    name = base or "model"
    suffix = 2
    while name in models:
        name = f"{base}-{suffix}"
        suffix += 1
    return name


def _discovered_model_entry(
    provider_name: str,
    model_id: str,
    enable: bool = False,
    source_metadata: dict[str, Any] | None = None,
) -> dict[str, Any]:
    params = _parameter_billions(model_id.lower())
    roles = _roles_for_discovered_model(provider_name, model_id, source_metadata or {})
    min_ram, recommended_ram, min_vram, recommended_vram = _resource_guess(params, roles)
    resource_source = "catalog_heuristic:parameter_size_and_role"
    overrides = _resource_requirements_from_source_metadata(source_metadata or {})
    if overrides:
        if overrides.get("min_ram_gb") is not None:
            min_ram = overrides["min_ram_gb"]
        if overrides.get("recommended_ram_gb") is not None:
            recommended_ram = overrides["recommended_ram_gb"]
        if overrides.get("min_vram_gb") is not None:
            min_vram = overrides["min_vram_gb"]
        if overrides.get("recommended_vram_gb") is not None:
            recommended_vram = overrides["recommended_vram_gb"]
        if isinstance(overrides.get("resource_estimate_source"), str) and overrides.get("resource_estimate_source"):
            resource_source = str(overrides["resource_estimate_source"])
        else:
            resource_source = "provider_catalog:source_metadata"
    preferred_runtime = _preferred_runtime_for_discovered_roles(provider_name, roles)
    managed_sources = {
        "openai",
        "anthropic",
        "azure_openai",
        "ollama_cloud",
        "azure_speech",
        "elevenlabs",
    }
    entry: dict[str, Any] = {
        "provider": (provider_name if provider_name in managed_sources else preferred_runtime),
        "model": model_id,
        "roles": roles,
        "local": provider_name not in managed_sources,
        "enabled": enable,
        "source": provider_name,
        "preferred_runtime": preferred_runtime,
        "notes": "Discovered from model provider and imported into the editable profile catalog.",
        "imported_by": "aiplane_refresh",
        "min_ram_gb": min_ram,
        "recommended_ram_gb": recommended_ram,
        "min_vram_gb": min_vram,
        "resource_estimate_source": resource_source,
        "source_metadata": source_metadata or {},
    }
    supported_runtimes = _supported_runtimes_for_discovered_roles(roles)
    if supported_runtimes:
        entry["supported_runtimes"] = supported_runtimes
    if recommended_vram is not None:
        entry["recommended_vram_gb"] = recommended_vram
    if provider_name == "ollama":
        entry["pull_command"] = f"ollama pull {model_id}"
    elif provider_name == "huggingface":
        entry["pull_command"] = (
            f"python -c \"from huggingface_hub import snapshot_download; snapshot_download('{model_id}')\""
        )
    elif provider_name == "huggingface_gguf":
        entry["pull_command"] = (
            f"python -c \"from huggingface_hub import snapshot_download; snapshot_download('{model_id}')\""
        )
    elif provider_name == "civitai":
        entry["pull_command"] = (
            "download via Civitai model/version API and place checkpoint in the target runtime model directory"
        )
    if any(
        role in roles
        for role in [
            "embedding",
            "text_to_speech",
            "speech_to_text",
            "image_generation",
            "video_generation",
        ]
    ):
        entry["capability_scores"] = _heuristic_capability_scores(entry)
        entry["capability_score_source"] = "catalog_heuristic"
    return entry


def _merge_source_discovered_model(
    existing: dict[str, Any],
    provider_name: str,
    model_id: str,
    source_metadata: dict[str, Any] | None = None,
) -> dict[str, Any]:
    merged = dict(existing)
    discovered = _discovered_model_entry(
        provider_name,
        model_id,
        enable=bool(existing.get("enabled", True)),
        source_metadata=source_metadata or {},
    )
    source_fields = [
        "model",
        "source",
        "local",
        "pull_command",
        "source_metadata",
    ]
    if _is_refresh_imported_model(existing):
        source_fields.extend(
            [
                "provider",
                "preferred_runtime",
                "roles",
                "notes",
                "imported_by",
                "min_ram_gb",
                "recommended_ram_gb",
                "min_vram_gb",
                "recommended_vram_gb",
                "resource_estimate_source",
            ]
        )
        if "capability_scores" not in existing and "capability_scores" in discovered:
            source_fields.extend(["capability_scores", "capability_score_source"])
    for field in source_fields:
        if field in discovered:
            merged[field] = discovered[field]
        elif field in {"recommended_vram_gb"} and field in merged:
            merged.pop(field, None)
    return merged


def _is_refresh_imported_model(model: dict[str, Any]) -> bool:
    if model.get("imported_by") == "aiplane_refresh":
        return True
    notes = str(model.get("notes") or "")
    return notes.startswith("Discovered from model provider") or notes.startswith("Discovered from provider")


