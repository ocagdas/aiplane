    def refresh_all(
        self,
        write: bool = True,
        enable: bool = True,
        include_empty_providers: bool = False,
        online: bool = True,
        query: str | None = None,
        limit: int = 500,
        provider_limits: dict[str, int] | None = None,
        progress: Callable[[str, str, str], None] | None = None,
        verbose: bool = False,
    ) -> dict[str, Any]:
        from .providers import ProviderRegistry

        provider_rows = ProviderRegistry(self.profile).list(include_empty=True)
        results: dict[str, Any] = {}
        skipped_providers = []
        for provider_row in provider_rows:
            provider_name = str(provider_row["name"])
            if provider_name == "local_file":
                continue
            if provider_row.get("enabled") is False:
                skipped_providers.append(
                    {
                        "name": provider_name,
                        "reason": "model provider is disabled",
                        "typical_runtimes": provider_row.get("typical_runtimes", []),
                        "source_contacted": False,
                    }
                )
                continue
            try:
                provider_limit = int((provider_limits or {}).get(provider_name, limit))
                result = self.refresh(
                    provider_name,
                    write=write,
                    enable=enable,
                    online=online,
                    query=query,
                    limit=provider_limit,
                    progress=progress,
                    verbose=verbose,
                )
                provider_result = result.get("results", {}).get(provider_name, {})
                results[provider_name] = provider_result
            except Exception as exc:  # noqa: BLE001 - all-provider refresh should report provider-specific failures.
                results[provider_name] = {
                    "ownership": "self_managed",
                    "status": "failed",
                    "write": write,
                    "new_entries_enabled": enable,
                    "source_contacted": False,
                    "prune_enabled": False,
                    "source_discovery_method": "error",
                    "source_discovery_reason": str(exc),
                    "source_models_returned": 0,
                    "profile_models_before_refresh": len(
                        [model for model in self.models().values() if model_source(model) == provider_name]
                    ),
                    "profile_curated_models_before_refresh": len(
                        [
                            model
                            for model in self.models().values()
                            if model_source(model) == provider_name and not _is_refresh_imported_model(model)
                        ]
                    ),
                    "profile_refresh_imported_models_before_refresh": len(
                        [
                            model
                            for model in self.models().values()
                            if model_source(model) == provider_name and _is_refresh_imported_model(model)
                        ]
                    ),
                    "source_models_already_profiled": 0,
                    "source_models_to_import": 0,
                    "source_models_to_update": 0,
                    "changes": {
                        "imported": 0,
                        "would_import": 0,
                        "updated": 0,
                        "would_update": 0,
                        "removed": 0,
                        "would_remove": 0,
                    },
                    "model_changes_count": 0,
                    "error": str(exc),
                }
        rows = list(results.values())
        changes = {
            "imported": sum(int(row.get("changes", {}).get("imported", 0)) for row in rows),
            "would_import": sum(int(row.get("changes", {}).get("would_import", 0)) for row in rows),
            "updated": sum(int(row.get("changes", {}).get("updated", 0)) for row in rows),
            "would_update": sum(int(row.get("changes", {}).get("would_update", 0)) for row in rows),
            "removed": sum(int(row.get("changes", {}).get("removed", 0)) for row in rows),
            "would_remove": sum(int(row.get("changes", {}).get("would_remove", 0)) for row in rows),
        }
        return {
            "name": "model_catalog_refresh_all",
            "write": write,
            "new_entries_enabled": enable,
            "online": online,
            "query": query,
            "limit": limit,
            "verbose": verbose,
            "provider_limits": provider_limits or {},
            "providers_total": len(rows),
            "providers_updated": sum(1 for row in rows if row.get("status") == "updated"),
            "providers_would_update": sum(1 for row in rows if row.get("status") == "would_update"),
            "providers_failed": sum(1 for row in rows if row.get("status") == "failed"),
            "providers_skipped_empty": len(skipped_providers),
            "changes": changes,
            "results": results,
            "skipped_providers": skipped_providers,
            "next_steps": _refresh_next_steps(write, changes),
        }

    def promote_generated(
        self,
        name: str,
        new_name: str | None = None,
        write: bool = False,
        keep_discovered: bool = True,
        overwrite: bool = False,
    ) -> dict[str, Any]:
        if not name or "/" in name or "\\" in name:
            raise ValueError("model entry name must be a simple name")
        target = new_name or name
        if not target or "/" in target or "\\" in target:
            raise ValueError("target model entry name must be a simple name")
        curated_models = _dict_of_dicts(self.config.get("models", {}))
        generated_models = _dict_of_dicts(self.generated_config.get("models", {}))
        if name not in generated_models:
            raise ValueError(f"discovered model entry not found: {name}")
        target_exists = target in curated_models
        if target_exists and not overwrite:
            raise ValueError(
                f"profile-owned model entry already exists: {target}; pass overwrite=True or use --overwrite after reviewing the existing entry"
            )
        promoted = dict(generated_models[name])
        promoted.pop("imported_by", None)
        promoted["promoted_from"] = name
        promoted["discovered_entry"] = name
        promoted["curated"] = True
        if write:
            curated_models[target] = promoted
            self.config["models"] = curated_models
            self.store.write_curated(self.config)
            if not keep_discovered:
                generated_models.pop(name, None)
                self.generated_config["models"] = generated_models
                self._write_generated_config()
        return {
            "name": "model_catalog_promote",
            "source": name,
            "target": target,
            "write": write,
            "keep_discovered": keep_discovered,
            "overwrite": overwrite,
            "target_exists": target_exists,
            "promoted": 1 if write else 0,
            "would_promote": 0 if write else 1,
            "removed_discovered": bool(write and not keep_discovered),
            "path": str(self.profile.root / "models.yaml"),
            "discovered_path": str(self._generated_path()),
            "model": promoted,
            "next_steps": _promote_next_steps(name, target, write, keep_discovered),
        }

    def remove_model(
        self,
        name: str,
        write: bool = True,
    ) -> dict[str, Any]:
        _validate_model_entry_name(name, "model entry name")
        curated_models = _dict_of_dicts(self.config.get("models", {}))
        if name not in curated_models:
            raise ValueError(f"profile-owned model entry not found: {name}")
        defaults = self.config.get("defaults", {})
        removed_defaults: list[str] = []
        if isinstance(defaults, dict):
            removed_defaults = sorted(role for role, value in defaults.items() if str(value) == name)
        if write:
            curated_models.pop(name, None)
            if isinstance(defaults, dict):
                for role in removed_defaults:
                    defaults.pop(role, None)
                self.config["defaults"] = defaults
            self.config["models"] = curated_models
            self._write_curated_config()
        return {
            "name": "model_catalog_remove",
            "entry": name,
            "write": write,
            "removed_curated": bool(write),
            "would_remove_curated": bool(not write),
            "removed_defaults": removed_defaults if write else [],
            "would_remove_defaults": removed_defaults if not write else [],
            "path": str(self.profile.root / "models.yaml"),
        }

    def clear_imported(
        self,
        provider_name: str | None = None,
        write: bool = False,
        include_curated: bool = True,
    ) -> dict[str, Any]:
        curated_models = _dict_of_dicts(self.config.get("models", {}))
        generated_models = _dict_of_dicts(self.generated_config.get("models", {}))
        removed_counts: dict[str, int] = {}
        removed_curated_counts: dict[str, int] = {}
        curated_dirty = False
        generated_dirty = False
        for name, model in list(generated_models.items()):
            if not isinstance(model, dict):
                continue
            source = model_source(model)
            if provider_name and source != provider_name:
                continue
            removed_counts[source] = removed_counts.get(source, 0) + 1
            if write:
                generated_models.pop(name, None)
                generated_dirty = True
        for name, model in list(curated_models.items()):
            if not isinstance(model, dict):
                continue
            is_imported = _is_refresh_imported_model(model)
            if not is_imported and not include_curated:
                continue
            source = model_source(model)
            if provider_name and source != provider_name:
                continue
            removed_counts[source] = removed_counts.get(source, 0) + 1
            if not is_imported:
                removed_curated_counts[source] = removed_curated_counts.get(source, 0) + 1
            if write:
                curated_models.pop(name, None)
                curated_dirty = True
        total_removed = sum(removed_counts.values())
        if write and total_removed:
            self.config["models"] = curated_models
            self.generated_config["models"] = generated_models
            if curated_dirty:
                self.store.write_curated(self.config)
            if generated_dirty:
                self._write_generated_config()
        provider_counts = [{"name": source, "count": count} for source, count in sorted(removed_counts.items())]
        curated_provider_counts = [
            {"name": source, "count": count} for source, count in sorted(removed_curated_counts.items())
        ]
        return {
            "name": "model_catalog_clear_cache",
            "write": write,
            "provider": provider_name,
            "include_curated": include_curated,
            "removed": total_removed if write else 0,
            "would_remove": 0 if write else total_removed,
            "provider_counts": provider_counts,
            "curated_provider_counts": curated_provider_counts,
            "path": str(self.profile.root / "models.yaml"),
            "discovered_path": str(self._generated_path()),
        }

